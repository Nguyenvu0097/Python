x = int(input("Nhập số nguyên thứ 1: "))
y = int(input("Nhập số nguyên thứ 2: "))
z = int(input("Nhập số nguyên thứ 3: "))

print("\n--- Kết quả ---")
print(f"a) Tổng = {x + y + z}")
print(f"   Tích = {x * y * z}")

print(f"b) Hiệu của {x} và {y} = {x - y}")

print(f"c) {x} chia {y}:")
print(f"   Phần nguyên = {x // y}")
print(f"   Phần dư     = {x % y}")
print(f"   Kết quả chính xác = {x / y}")